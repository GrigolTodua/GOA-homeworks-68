a = 5        # int
b = 2.5      # float
result = a + b  # Python გარდაქმნის 'a'-ს float-ად ავტომატურად
print(result)   # 7.5

x = 10
y = True       # Bool გადაიქცევა 1-ად
print(x + y)   # 11

value = 100
result = value / 2     # int / int = float
print(result)          # 50.0

n = 3
m = 4.0
z = n * m      # int გარდაიქმნება float-ად
print(z)       # 12.0

a = False
b = 10
print(a + b)   # False = 0 → 0 + 10 = 10
