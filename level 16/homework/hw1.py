# ჩამოწერეთ ყველა შედარების და ლოგიკური ოპერატორები, თითოეულს მიუწერეთ ახსნა(დააკონსპექტეთ)
print(5 == 5)   # True  (equal)
print(5 != 3)   # True  (not equal)
print(7 > 5)    # True  (greater than)
print(4 < 6)    # True  (less than)
print(5 >= 5)   # True  (greater or equal)
print(3 <= 3)   # True  (less or equal)

# ლოგიკური ოპერატორები
print(5 > 2 and 10 > 5)  # True  (both true)
print(5 > 2 or 10 < 5)   # True  (one true)
print(not True)          # False (opposite)
